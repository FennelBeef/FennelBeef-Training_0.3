function funchangxiang() {
  var tbody = document.querySelector(".tbody");
  var item = document.createElement("div");
  item.className = "item";
  var p_checkbox = document.createElement("div");
  p_checkbox.className = "p-checkbox";
  var input = document.createElement("input");
  input.type = "checkbox";
  var p_goods = document.createElement("div");
  p_goods.className = "p-goods";
  var img = document.createElement("img");
  img.src = "../img/Product/畅享/畅享50pro.jpg";
  var p = document.createElement("p");
  p.innerHTML = "华为畅享 50 Pro";
  var p_price = document.createElement("div");
  p_price.className = "p-price";
  p_price.innerHTML = "￥996.00";
  var p_delete = document.createElement("div");
  p_delete.className = "p-delete";
  p_delete.innerHTML = "删除";
  
  tbody.appendChild(item);
  p_checkbox.appendChild(input);
  item.appendChild(p_checkbox);
  p_goods.appendChild(img);
  p_goods.appendChild(p);
  item.appendChild(p_goods);
  item.appendChild(p_price);
  item.appendChild(p_delete);
  //  var id = document.createElement("div");
  //  item.appendChild(id);
}


